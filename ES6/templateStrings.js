//In es5 to concatenate two strings we use '+'
console.log("My name is Vaishnav" +" ,"+" and my age is 23") //My name is Vaishnav , and my age is 23

//In es6 we don't need '+' to concatenate two strings and we use ``
console.log(`My name is Vaishnav and my age is 23`) //My name is Vaishnav , and my age is 23

let firstname="vaishnav"
let lastname="puram"
//In es5
console.log("First name:" +firstname +" lastname:" + lastname) //First name:vaishnav lastname:puram
//In es6
console.log(`First name:${firstname} lastname:${lastname}`) //First name:vaishnav lastname:puram

